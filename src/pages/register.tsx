import type { NextPage } from "next";
import Head from "next/head";
import Link from "next/link";
import { useCallback, useState } from "react";
import { useRouter } from "next/router";
import { useForm } from "react-hook-form";
import { zodResolver } from "@hookform/resolvers/zod";
import isStrongPassword from "validator/lib/isStrongPassword";

import { trpc } from "../utils/trpc";
import { ISignUp, signUpSchema } from "../utils/validations/auth";

const SignUp: NextPage = () => {
  const router = useRouter();
  const {
    register,
    handleSubmit,
    formState: { errors },
  } = useForm<ISignUp>({
    resolver: zodResolver(signUpSchema),
    shouldUseNativeValidation: true, // need for :invalid tag tailwind
    criteriaMode: "all",
  });

  const { mutateAsync } = trpc.useMutation(["user.createNewClient"]);

  const [emailAlreadyExists, setEmailAlreadyExists] = useState(false);

  const onSubmit = useCallback(
    async (data: ISignUp) => {
      try {
        const result = await mutateAsync(data);
        if (result.status === 201) {
          router.push("/");
        }
      } catch (error) {
        setEmailAlreadyExists(true);
      }
    },
    [mutateAsync, router],
  );

  console.log({ errors });
  return (
    <div>
      <Head>
        <title>Next App - Register</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="flex flex-col items-center justify-center">
        <form
          className="flex h-screen w-full max-w-sm items-center justify-center"
          onSubmit={handleSubmit(onSubmit)}
        >
          <div className="m-64">
            <div className="p-12 shadow-xl">
              <h2 className="mb-6 ml-6 cursor-default text-center text-xl font-bold text-kym3">
                Create an account!
              </h2>
              <div className=" m-6">
                <input
                  type="text"
                  placeholder="Nombre"
                  className="mb-4 border-l-4 border-l-button bg-gray-100 py-1 px-8 invalid:border-2 invalid:border-red-500"
                  title=""
                  {...register("username")}
                />
                {errors.username && (
                  <p className="text-red-500">{errors.username.message}</p>
                )}
                <input
                  type="email"
                  placeholder="Email"
                  className="mb-4 border-l-4 border-l-button bg-gray-100 py-1 px-8 invalid:border-2 invalid:border-red-500"
                  title=""
                  {...register("email", {
                    onChange: () => setEmailAlreadyExists(false),
                  })}
                />
                {errors.email && (
                  <p className="text-red-500">{errors.email.message}</p>
                )}
                {emailAlreadyExists && (
                  <p className="font-semibold text-red-500">
                    El email ya esta siendo usado
                  </p>
                )}
                <input
                  type="password"
                  placeholder="Type your password..."
                  className="mb-2 border-l-4 border-l-button bg-gray-100 py-1 px-8"
                  {...register("password")}
                />
                {errors.password && (
                  <p className="text-red-500">{errors.password.message}</p>
                )}
              </div>
              <div className="flex items-center justify-center">
                <div className="m-auto text-center">
                  <Link href="/">
                    <p className=" cursor-pointer  font-semibold text-gray-600">
                      Go to login
                    </p>
                  </Link>
                  <button
                    className="m-2 mt-3 block rounded bg-button py-1 pl-20 pr-20 font-semibold text-white hover:bg-button_hover"
                    type="submit"
                  >
                    Sign Up
                  </button>
                </div>
              </div>
            </div>
          </div>
        </form>
      </main>
    </div>
  );
};

export default SignUp;
