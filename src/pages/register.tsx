import type { NextPage } from "next";
import Head from "next/head";
import Link from "next/link";
import { useCallback } from "react";
import { useRouter } from "next/router";
import { useForm } from "react-hook-form";
import { zodResolver } from "@hookform/resolvers/zod";

import { trpc } from "../utils/trpc";
import { ISignUp, signUpSchema } from "../utils/validations/auth";

const SignUp: NextPage = () => {
  const router = useRouter();
  const { register, handleSubmit } = useForm<ISignUp>({
    resolver: zodResolver(signUpSchema),
  });

  const { mutateAsync } = trpc.useMutation(["user.createNewClient"]);

  const onSubmit = useCallback(
    async (data: ISignUp) => {
      const result = await mutateAsync(data);
      if (result.status === 201) {
        router.push("/");
      }
    },
    [mutateAsync, router],
  );

  return (
    <div>
      <Head>
        <title>Next App - Register</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className="flex flex-col items-center justify-center">
        <form
          className="flex h-screen w-full max-w-sm items-center justify-center"
          onSubmit={handleSubmit(onSubmit)}
        >
          <div className="m-64">
            <div className="p-12 shadow-xl">
              <h2 className="mb-6 ml-6 cursor-default text-center text-xl font-bold text-kym3">
                Create an account!
              </h2>
              <div className=" m-6">
                <input
                  type="text"
                  placeholder="Type your username..."
                  className="mb-4 border-l-4 border-l-button bg-gray-100 py-1 px-8"
                  {...register("username")}
                />
                <input
                  type="email"
                  placeholder="Type your email..."
                  className="mb-4 border-l-4 border-l-button bg-gray-100 py-1 px-8"
                  {...register("email")}
                />
                <input
                  type="password"
                  placeholder="Type your password..."
                  className="mb-2 border-l-4 border-l-button bg-gray-100 py-1 px-8"
                  {...register("password")}
                />
              </div>
              <div className="flex items-center justify-center">
                <div className="m-auto text-center">
                  <Link href="/">
                    <p className=" cursor-pointer  font-semibold text-gray-600">
                      Go to login
                    </p>
                  </Link>
                  <button
                    className="m-2 mt-3 block rounded bg-button py-1 pl-20 pr-20 font-semibold text-white hover:bg-button_hover"
                    type="submit"
                  >
                    Sign Up
                  </button>
                </div>
              </div>
            </div>
          </div>
        </form>
      </main>
    </div>
  );
};

export default SignUp;
